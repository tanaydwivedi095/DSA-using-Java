/*
 Data Types:
    1. Primitive Data Type - byte, short, char, boolean, int, long, float, double
    2. Non-Primitive Data Type - String, Array, Class, Object, Interface

 Input in Java:
    a. next
    b. nextLine
    c. nextInt
    d. nextByte
    e. nextFloat
    f. nextDouble
    g. nextBoolean
    h. nextShort
    i. nextLong

 Type Conversion: (Widening Conversion / Implicit Conversion)
    Conversion Happens when:
        a. type compatible
        b. destination type > source type
    byte -> short -> int -> float -> long -> double

 Type Casting: (Narrowing Conversion / Explicit Conversion)
    When we try to convert a larger data type to a smaller data type explicitly

 Type Promotion in Expressions:
    1. Java automatically promotes each byte, short or char operand to int when evaluating an expression
    2. If one operand in long, float or double the whole expression is promoted to long, float or double respectively
    
*/

